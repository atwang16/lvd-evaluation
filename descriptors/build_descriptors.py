import subprocess
import sys
import os

ROOT_PATH = os.path.abspath(os.path.join(os.getcwd(), os.pardir))
results_path = None
image_db_path = None
detector_db_path = None
descriptors_parameter_file = None
descriptor_executable = None
destination_path = None
overwrite_flag = "0"

def generate_descriptors(desc, img_db, det_db):
    global descriptor_executable, descriptors_parameter_file, image_db_path, detector_db_path, destination_path, overwrite_flag
    args = [descriptor_executable, desc, descriptors_parameter_file, image_db_path, detector_db_path, destination_path, overwrite_flag]
    print(desc + ": " + "extracting descriptors from " + img_db + ", with keypoints generated by " + det_db)
    print("***")
    subprocess.run(args)


# MAIN #

if __name__ == "__main__":
    if len(sys.argv) < 4:
        print("Usage: python3 build_descriptors.py detector_name desc_name database_name [-overwrite]")
        sys.exit(1)

    detector = sys.argv[1]
    desc_name = sys.argv[2]
    database = sys.argv[3]

    if len(sys.argv) >= 5:
        overwrite_flag = str(1 if (sys.argv[4] == "-overwrite") else 0)

    if detector == desc_name:
        res_folder_name = detector
    else:
        res_folder_name = detector + "_" + desc_name

    with open(os.path.join(ROOT_PATH, "project_structure.txt")) as f:
        line = f.readline()
        while line != "":
            line_split = line.split("=")
            var = line_split[0]
            directory = line_split[-1]
            if directory[-1] == "\n":
                directory = directory[:-1]
            if var == "RESULTS_FOLDER":
                results_path = os.path.join(ROOT_PATH, directory)
            elif var == "DATASETS_FOLDER":
                image_all_db_path = os.path.join(ROOT_PATH, directory)
            elif var == "DESCRIPTORS_FOLDER":
                descriptors_parameter_file = os.path.join(ROOT_PATH, directory, desc_name + "_parameters.txt")
            elif var == "DESCRIPTORS_EXECUTABLE":
                descriptor_executable = os.path.join(ROOT_PATH, directory)
            line = f.readline()

    if results_path is None or image_all_db_path is None or \
        descriptors_parameter_file is None or descriptor_executable is None:
        print("Error: could not find all of the necessary directory paths from the following file:")
        print(" ", os.path.join(ROOT_PATH, "project_structure.txt"))
        sys.exit(1)

    detector_db_path = os.path.join(results_path, detector, database)
    image_db_path = os.path.join(image_all_db_path, database)
    destination_path = os.path.join(results_path, res_folder_name)

    generate_descriptors(desc_name, database, detector)